def create_cover_image(title, author, filename, color_hex):
    """创建封面图片"""
    # 创建图片
    width, height = 300, 400
    img = Image.new('RGB', (width, height), color=color_hex)
    draw = ImageDraw.Draw(img)
    
    # 尝试使用系统字体
    try:
        # macOS 系统字体
        title_font = ImageFont.truetype("/System/Library/Fonts/PingFang.ttc", 32)
        author_font = ImageFont.truetype("/System/Library/Fonts/PingFang.ttc", 20)
    except:
        # 如果失败则使用默认字体
        title_font = ImageFont.load_default()
        author_font = ImageFont.load_default()
    
    # 绘制标题（居中）
    title_bbox = draw.textbbox((0, 0), title, font=title_font)
    title_width = title_bbox[2] - title_bbox[0]
    title_height = title_bbox[3] - title_bbox[1]
    title_x = (width - title_width) // 2
    title_y = height // 3
    draw.text((title_x, title_y), title, fill='white', font=title_font)
    
    # 绘制作者（居中）
    author_bbox = draw.textbbox((0, 0), author, font=author_font)
    author_width = author_bbox[2] - author_bbox[0]
    author_x = (width - author_width) // 2
    author_y = title_y + title_height + 30
    draw.text((author_x, author_y), author, fill='white', font=author_font)
    
    # 保存图片
    img.save(filename)
    print(f"✓ 已创建封面: {filename}")

