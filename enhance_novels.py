def update_existing_novels(cursor):
    """更新现有小说的封面和章节"""
    # 获取现有小说
    cursor.execute('SELECT id, title, author FROM novels')
    novels = cursor.fetchall()
    
    colors = {
        '星辰变': '#4a90e2',
        '斗破苍穹': '#e74c3c',
        '凡人修仙传': '#2ecc71',
        '鬼吹灯': '#9b59b6',
        '盗墓笔记': '#34495e'
    }
    
    for novel_id, title, author in novels:
        # 更新封面
        if title in colors:
            cover_filename = f"covers/{title}.jpg"
            if not os.path.exists(cover_filename):
                create_cover_image(title, author, cover_filename, colors[title])
                cursor.execute('UPDATE novels SET cover_url = ? WHERE id = ?', 
                             (cover_filename, novel_id))
                print(f"✓ 已更新《{title}》的封面")
        
        # 检查章节内容
        cursor.execute('SELECT chapter_number, content FROM chapters WHERE novel_id = ? ORDER BY chapter_number', 
                      (novel_id,))
        chapters = cursor.fetchall()
        
        # 更新章节内容（如果内容过短）
        for chapter_num, content in chapters:
            if len(content) < 500:  # 如果章节内容少于500字
                # 扩充内容
                extended_content = content + "\n\n" + generate_extended_content(title, chapter_num)
                cursor.execute('UPDATE chapters SET content = ? WHERE novel_id = ? AND chapter_number = ?',
                             (extended_content, novel_id, chapter_num))
        
        print(f"✓ 已检查《{title}》的章节内容")

